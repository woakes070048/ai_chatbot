def _extract_keywords(text: str) -> list[str]:
	"""Extract meaningful keywords from text (words >= 4 chars, no stopwords).

	Args:
		text: Lowercased text to extract keywords from.

	Returns:
		List of keyword strings.
	"""
	words = re.findall(r"[a-z]{4,}", text)
	return [w for w in words if w not in _STOPWORDS]


def _parse_session_context(raw) -> dict:
	"""Parse session_context JSON safely.

	Args:
		raw: Raw JSON string or dict from DB.

	Returns:
		Parsed dict, or empty dict on failure.
	"""
	if not raw:
		return {}
	try:
		return json.loads(raw) if isinstance(raw, str) else raw
	except (json.JSONDecodeError, TypeError):
		return {}


def _compute_match_score(msg_words: set[str], summary: str, topics: list[str]) -> int:
	"""Compute a simple keyword overlap score.

	Each matching keyword in summary or topic labels adds 1 point.
	Topic matches count double because they're more concentrated signals.

	Args:
		msg_words: Set of keywords from the user's message.
		summary: Conversation summary text.
		topics: List of topic labels.

	Returns:
		Integer score (higher = more relevant).
	"""
	score = 0
	summary_lower = summary.lower()

	# Score keyword matches in summary
	for word in msg_words:
		if word in summary_lower:
			score += 1

	# Score keyword matches in topics (double weight)
	topics_text = " ".join(topics).lower()
	for word in msg_words:
		if word in topics_text:
			score += 2

	return score
